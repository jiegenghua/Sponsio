"""Renderers for :class:`~sponsio.reporting.Report`.

Three output formats:

* ``markdown`` — for Slack, GitHub issues, PR comments.
* ``html`` — for dashboards, email, or embedding in a ``<details>`` block.
* ``json`` — for machine consumption (CI, scripts, downstream tools).

All three are pure string-producing functions with no external
dependencies.  Zero Jinja — keeps the ``sponsio/`` core zero-dep
invariant intact.
"""

from __future__ import annotations

import html as _html
import json
from dataclasses import asdict
from datetime import datetime, timezone

from sponsio.reporting.aggregator import ContractStat, Report


# ---------------------------------------------------------------------------
# Dispatcher
# ---------------------------------------------------------------------------


def render(report: Report, fmt: str = "markdown") -> str:
    """Render ``report`` in the requested format.

    Args:
        report: The aggregated report.
        fmt: One of ``"markdown"`` / ``"md"``, ``"html"``, ``"json"``.

    Raises:
        ValueError: On unknown format.
    """
    fmt = fmt.lower()
    if fmt in ("markdown", "md"):
        return render_markdown(report)
    if fmt == "html":
        return render_html(report)
    if fmt == "json":
        return render_json(report)
    raise ValueError(f"Unknown --format: {fmt!r}. Choose markdown, html, or json.")


# ---------------------------------------------------------------------------
# Shared helpers
# ---------------------------------------------------------------------------


def _ts_utc(ts: float) -> str:
    """Format unix seconds as ``YYYY-MM-DD HH:MM UTC`` (empty on 0)."""
    if ts <= 0:
        return "—"
    return datetime.fromtimestamp(ts, tz=timezone.utc).strftime("%Y-%m-%d %H:%M UTC")


def _agent_title(report: Report) -> str:
    if not report.agents:
        return "—"
    if len(report.agents) == 1:
        return report.agents[0]
    return f"{len(report.agents)} agents"


def _severity(c: ContractStat) -> str:
    """Heuristic one-glyph severity for a contract row."""
    if c.blocked:
        return "🛑 blocked"
    if c.observed:
        return "👁 observed"
    if c.retrying:
        return "🔁 retry"
    return "—"


# ---------------------------------------------------------------------------
# Markdown
# ---------------------------------------------------------------------------


def render_markdown(report: Report) -> str:
    """Render a Slack-friendly Markdown report."""
    lines: list[str] = []
    title_agent = _agent_title(report)
    lines.append(f"# Sponsio Report — {title_agent}")
    lines.append("")
    lines.append(
        f"**Window:** {_ts_utc(report.window_start)} → {_ts_utc(report.window_end)}"
    )
    lines.append(f"**Sessions:** {report.total_sessions}")
    lines.append(f"**Events evaluated:** {report.total_events}")
    lines.append(f"**Violations caught:** {report.violations}")
    lines.append(f"**Actually blocked (enforce mode):** {report.blocked}")
    lines.append(f"**Would-have-blocked (observe mode):** {report.observed}")
    lines.append(f"**Sto retries:** {report.retrying}")
    lines.append(f"**Pass rate:** {report.pass_rate * 100:.1f}%")
    lines.append("")

    # Top violations table
    if report.by_contract:
        lines.append("## Top Violations")
        lines.append("")
        lines.append("| Contract | Pipeline | Violations | Severity | First seen |")
        lines.append("|---|---|---:|---|---|")
        for c in report.by_contract[:20]:
            contract_text = c.constraint.replace("|", "\\|")
            lines.append(
                f"| {contract_text} | {c.pipeline} | {c.violations} | "
                f"{_severity(c)} | {_ts_utc(c.first_seen)} |"
            )
        if len(report.by_contract) > 20:
            lines.append(f"| _…{len(report.by_contract) - 20} more_ | | | | |")
        lines.append("")

    # Most-violating sessions
    worst = [s for s in report.by_session if s.violations > 0][:5]
    if worst:
        lines.append("## Most-Violating Sessions")
        lines.append("")
        lines.append("| Session | Agent | Events | Violations |")
        lines.append("|---|---|---:|---:|")
        for s in worst:
            lines.append(
                f"| `{s.source}` | {s.agent_id} | {s.events} | {s.violations} |"
            )
        lines.append("")

    # What to do next
    lines.append("## What to do next")
    lines.append("")
    recs = _recommendations(report)
    for r in recs:
        lines.append(f"- {r}")
    lines.append("")

    lines.append("---")
    lines.append("*Generated by `sponsio report` — see `sponsio report --help`.*")
    return "\n".join(lines) + "\n"


def _recommendations(report: Report) -> list[str]:
    """Heuristic "what to do" bullets.  Must never be empty."""
    recs: list[str] = []
    if report.total_events == 0:
        return [
            "No shadow-mode events yet. Start your agent with "
            '`sponsio.Sponsio(..., mode="observe")` and run some traffic, '
            "then re-run `sponsio report`."
        ]
    if report.observed > 0:
        recs.append(
            f"🕶 **{report.observed} events would have been blocked** in "
            "enforce mode. Flip `SPONSIO_MODE=enforce` once you're "
            "comfortable with the catch rate."
        )
    if report.blocked > 0:
        recs.append(
            f"🛑 **{report.blocked} events blocked in production.** Review "
            "the top contracts below and tune the ones with high block "
            "counts — they may be too strict, or pointing at a real bug."
        )
    if report.retrying > 0:
        recs.append(
            f"🔁 **{report.retrying} sto retries.** The agent self-corrected, "
            "but persistent retries on the same contract are worth a look."
        )
    # Top contract advice
    if report.by_contract:
        top = report.by_contract[0]
        recs.append(
            f"⚠ Top offender: **{top.constraint}** ({top.violations} violations "
            f"across {top.pipeline} pipeline). Investigate first."
        )
    if report.pass_rate >= 0.99 and report.violations == 0:
        recs.append("✅ No violations in this window. Nothing to do.")
    return recs


