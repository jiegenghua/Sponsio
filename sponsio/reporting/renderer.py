_HTML_STYLE = """
<style>
  .sponsio-report { font-family: -apple-system, BlinkMacSystemFont, "Segoe UI",
      Helvetica, Arial, sans-serif; color: #1f2328; max-width: 860px; }
  .sponsio-report h1 { font-size: 1.5rem; margin-bottom: 0.25rem; }
  .sponsio-report .meta { color: #656d76; font-size: 0.9rem; margin-bottom: 1rem; }
  .sponsio-report table { border-collapse: collapse; width: 100%; margin: 0.5rem 0 1.25rem 0; }
  .sponsio-report th, .sponsio-report td { border: 1px solid #d0d7de;
      padding: 0.4rem 0.6rem; font-size: 0.9rem; text-align: left; }
  .sponsio-report th { background: #f6f8fa; }
  .sponsio-report .num { text-align: right; font-variant-numeric: tabular-nums; }
  .sponsio-report .pill { display: inline-block; padding: 0.1rem 0.45rem;
      border-radius: 0.75rem; font-size: 0.8rem; }
  .sponsio-report .pill-blocked { background: #ffebe9; color: #cf222e; }
  .sponsio-report .pill-observed { background: #fff8c5; color: #9a6700; }
  .sponsio-report .pill-retry { background: #ddf4ff; color: #0969da; }
</style>
""".strip()


def _pill_for(c: ContractStat) -> str:
    if c.blocked:
        return '<span class="pill pill-blocked">blocked</span>'
    if c.observed:
        return '<span class="pill pill-observed">observed</span>'
    if c.retrying:
        return '<span class="pill pill-retry">retry</span>'
    return "—"


def render_html(report: Report) -> str:
    """Render a self-contained HTML report (single ``<div>``, inline CSS)."""
    esc = _html.escape
    title_agent = esc(_agent_title(report))
    parts = [_HTML_STYLE, '<div class="sponsio-report">']
    parts.append(f"<h1>Sponsio Report — {title_agent}</h1>")
    parts.append(
        f'<div class="meta">Window: {esc(_ts_utc(report.window_start))} '
        f"→ {esc(_ts_utc(report.window_end))} &middot; "
        f"{report.total_sessions} sessions &middot; "
        f"{report.total_events} events &middot; "
        f"pass rate {report.pass_rate * 100:.1f}%</div>"
    )
    parts.append("<table>")
    parts.append(
        "<tr><th>Blocked</th><th>Observed</th><th>Sto retries</th>"
        "<th>Violations total</th></tr>"
    )
    parts.append(
        f'<tr><td class="num">{report.blocked}</td>'
        f'<td class="num">{report.observed}</td>'
        f'<td class="num">{report.retrying}</td>'
        f'<td class="num">{report.violations}</td></tr>'
    )
    parts.append("</table>")

    if report.by_contract:
        parts.append("<h2>Top violations</h2>")
        parts.append("<table>")
        parts.append(
            "<tr><th>Contract</th><th>Pipeline</th><th>Violations</th>"
            "<th>Severity</th><th>First seen</th></tr>"
        )
        for c in report.by_contract[:20]:
            parts.append(
                "<tr>"
                f"<td>{esc(c.constraint)}</td>"
                f"<td>{esc(c.pipeline)}</td>"
                f'<td class="num">{c.violations}</td>'
                f"<td>{_pill_for(c)}</td>"
                f"<td>{esc(_ts_utc(c.first_seen))}</td>"
                "</tr>"
            )
        parts.append("</table>")

    worst = [s for s in report.by_session if s.violations > 0][:5]
    if worst:
        parts.append("<h2>Most-violating sessions</h2>")
        parts.append("<table>")
        parts.append(
            "<tr><th>Session</th><th>Agent</th><th>Events</th><th>Violations</th></tr>"
        )
        for s in worst:
            parts.append(
                "<tr>"
                f"<td><code>{esc(s.source)}</code></td>"
                f"<td>{esc(s.agent_id)}</td>"
                f'<td class="num">{s.events}</td>'
                f'<td class="num">{s.violations}</td>'
                "</tr>"
            )
        parts.append("</table>")

    parts.append("</div>")
    return "\n".join(parts) + "\n"


# ---------------------------------------------------------------------------
# JSON
# ---------------------------------------------------------------------------


def render_json(report: Report) -> str:
    """Render the report as pretty-printed JSON (machine-readable)."""
    payload = {
        "agents": report.agents,
        "window": {
            "start": report.window_start,
            "end": report.window_end,
            "start_iso": _ts_utc(report.window_start),
            "end_iso": _ts_utc(report.window_end),
        },
        "totals": {
            "events": report.total_events,
            "sessions": report.total_sessions,
            "passed": report.passed,
            "blocked": report.blocked,
            "observed": report.observed,
            "retrying": report.retrying,
            "violations": report.violations,
            "pass_rate": report.pass_rate,
        },
        "by_contract": [asdict(c) for c in report.by_contract],
        "by_session": [asdict(s) for s in report.by_session],
    }
    return json.dumps(payload, indent=2, default=str) + "\n"
